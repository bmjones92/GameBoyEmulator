package org.guide.gameboy.cartridge;

/**
 * The destination code specifies where the game was intended to be sold.
 *
 * @author Brendan Jones
 */
public enum DestinationCode {

    /**
     * Indicates the game is intended to be exclusive to Japan.
     */
    JAPAN,

    /**
     * Indicates the game is intended to be sold worldwide.
     */
    WORLDWIDE

}
