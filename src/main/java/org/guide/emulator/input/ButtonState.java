package org.guide.emulator.input;

/**
 * Represents the different states a key or button can be in.
 *
 * @author Brendan Jones
 */
public enum ButtonState {
    /**
     * Indicates that the button or key is pressed.
     */
    PRESSED,
    /**
     * Indicates that the button or key is not pressed.
     */
    RELEASED
}
